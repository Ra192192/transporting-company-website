const form = document.querySelector("#contactForm");
const statusEl = document.querySelector("#formStatus");
const languageButtons = document.querySelectorAll("[data-lang]");
const metaDescription = document.querySelector('meta[name="description"]');

let currentLanguage = localStorage.getItem("siteLanguage") || "uk";

function translate(key) {
  return translations[currentLanguage][key] || translations.uk[key] || key;
}

function applyLanguage(language) {
  currentLanguage = translations[language] ? language : "uk";

  document.documentElement.lang = currentLanguage;
  document.title = translate("metaTitle");

  if (metaDescription) {
    metaDescription.setAttribute("content", translate("metaDescription"));
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = translate(element.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.setAttribute("placeholder", translate(element.dataset.i18nPlaceholder));
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    element.setAttribute("aria-label", translate(element.dataset.i18nAria));
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    element.setAttribute("alt", translate(element.dataset.i18nAlt));
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === currentLanguage;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", isActive ? "true" : "false");
  });

  localStorage.setItem("siteLanguage", currentLanguage);
}

function getValidationMessage(fieldErrors) {
  if (fieldErrors.phone) {
    return translate("phoneInvalid");
  }

  if (fieldErrors.name) {
    return translate("nameInvalid");
  }

  if (fieldErrors.message) {
    return translate("messageInvalid");
  }

  return translate("genericValidation");
}

function createUserError(message) {
  const error = new Error(message);
  error.userFacing = true;
  return error;
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

applyLanguage(currentLanguage);

form.addEventListener("submit", async (event) => {
  event.preventDefault();

  statusEl.className = "form-status";
  statusEl.textContent = translate("sending");

  const formData = new FormData(form);
  const payload = {
    name: formData.get("name").trim(),
    phone: formData.get("phone").trim(),
    message: formData.get("message").trim()
  };

  try {
    const response = await fetch("/api/contact", {
      method: "POST",
      headers: {
        "Content-Type": "application/json"
      },
      body: JSON.stringify(payload)
    });

    let data = {};
    try {
      data = await response.json();
    } catch (error) {
      data = {};
    }

    if (!response.ok) {
      throw createUserError(getValidationMessage(data.fieldErrors || {}));
    }

    statusEl.classList.add("success");
    statusEl.textContent = translate("success");
    form.reset();
  } catch (error) {
    statusEl.classList.add("error");
    statusEl.textContent = error.userFacing ? error.message : translate("submitError");
  }
});
